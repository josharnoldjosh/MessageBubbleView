import UIKit


public final class MessageBubbleView {

    let name = "MessageBubbleView"
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
}
